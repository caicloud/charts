# Exit on error. Append "|| true" if you expect an error.
set -o errexit
# Do not allow use of undefined vars. Use ${VAR:-} to use an undefined VAR
set -o nounset
# Catch the error in pipeline.
set -o pipefail

function move() {
	# Clear output dir.
	rm -rf $OUTPUT_DIR
	mkdir -p $OUTPUT_DIR
	cp -R $INPUT_DIR/* $OUTPUT_DIR
	echo "move $INPUT_DIR to $OUTPUT_DIR"
}

move
